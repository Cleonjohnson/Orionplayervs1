      id: `gen_${i}`,
      text: t[0],
      optionA: t[1],
      optionB: t[2],
      effectA: { votes: votesA, treasury: treasuryA, scandal: scandalA },
      effectB: { votes: votesB, treasury: treasuryB, scandal: scandalB },
